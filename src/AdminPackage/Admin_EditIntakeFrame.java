    public void backMainFrame(){
        Admin_MainFrame fr = new Admin_MainFrame();
        fr.setVisible(true);
        
        // Switch to allot intake advisors tab
        fr.updateIntakeMgmtPane(fr);
        int tabIndex = fr.TabPanel.getTabCount()-1;
        fr.changedTab(tabIndex);
    }
    
