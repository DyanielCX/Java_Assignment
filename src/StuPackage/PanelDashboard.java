private void readStudentDetailsFromFile(String fileName) {
    try (BufferedReader br = new BufferedReader(new FileReader(fileName))) {
        String line;
        while ((line = br.readLine()) != null) {
            String[] parts = line.split(",");
            if (parts[0].trim().equals(username)) { 
                StuID.setText(parts[0].trim());
                StuName.setText(parts[2].trim());
                StuAge.setText(parts[3].trim());
                StuIntake.setText(parts[4].trim());
                StuAssessment.setText(parts[5].trim());
                LecSupName.setText(parts[6].trim());
                return; // Exit the loop once the user is found
            }
        }
        // If the loop completes without finding the user
        System.err.println("User not found in the file.");
    } catch (IOException e) {
        System.err.println("Error reading the file: " + e.getMessage());
    }
}
    private boolean isPasswordCorrect(String username, String password) {
        try (BufferedReader br = new BufferedReader(new FileReader("StuData.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts[0].equals(username) && parts[1].equals(password)) {
                    return true;
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return false;
    }

    private boolean updatePasswordInFile(String username, String newPassword) {
        List<String> lines = new ArrayList<>();
        boolean updated = false;

       // Read the file and update the password for the matching username
        try (BufferedReader br = new BufferedReader(new FileReader("StuData.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts[0].equals(username)) {
                    parts[1] = newPassword;
                    line = String.join(",", parts);
                    updated = true;
                }
                lines.add(line);
            }
        } catch (IOException e) {
            e.printStackTrace();
            return false;
        }

        // If the username was found and updated, write the changes back to the file
        if (updated) {
            try (BufferedWriter bw = new BufferedWriter(new FileWriter("StuData.txt"))) {
                for (String updatedLine : lines) {
                    bw.write(updatedLine);
                    bw.newLine();
                }
            } catch (IOException e) {
                e.printStackTrace();
                return false;
            }
        }

    return updated;
    }
